static void ping_mdm_rpccbprog_0x00010002( struct svc_req *rqstp, xdr_s_type *srv )
{
  switch( rqstp->rq_proc ) {
    case ONCRPC_PING_MDM_DATA_CB_F_TYPE_PROC:
      ping_mdm_data_cb_f_type_svc_0x00010002( srv );
      break;
    case ONCRPC_PING_MDM_CB_F_TYPE_PROC:
      ping_mdm_cb_f_type_svc_0x00010002( srv );
      break;
    default:
      // invalid RPC procedure number
      (void) XDR_MSG_DONE( srv );
      svcerr_default_err( srv, rqstp, ping_mdm_rpc_api_versions  );
      break;
  }

  oncrpcxdr_mem_free( srv );
} /* ping_mdm_rpccbprog_0x00010002 */

static void ping_mdm_data_cb_f_type_svc_0x00010002( xdr_s_type *srv )
{
  ping_mdm_data_cb_f_type *cmd_cb_func;
  uint32  cb_id;
  boolean xdr_status = TRUE;
  int     xdr_op_number = 0;

  unsigned long int i;
  void *memset_temp;
  unsigned long int length_unsigned_long_int;
  uint32 *data = NULL;
  uint32 size_words;
  uint32 sum;

  boolean cmd_cb_func_result;


  /* XDR OP NUMBER = 1 */
  xdr_op_number = 1;
  xdr_status = XDR_RECV_UINT32( srv, &cb_id );

  /* XDR OP NUMBER = 2 */
  if ( xdr_status )
  {
    xdr_op_number = 2;
    xdr_status = XDR_RECV_UINT32( srv, &length_unsigned_long_int );

    /* XDR OP NUMBER = 3 */
    if ( xdr_status && length_unsigned_long_int > 0 )
    {
      xdr_op_number  = 3;
      memset_temp = oncrpcxdr_mem_alloc( srv, length_unsigned_long_int * sizeof( *data ));
      memset(memset_temp, 0, length_unsigned_long_int * sizeof( *data ));
      data = memset_temp;


      /* Calling array of XDR routines */
      for ( i = 0; xdr_status && i < (length_unsigned_long_int); i++ ) {
        /*lint -save -e545*/
        xdr_status = XDR_RECV_UINT32( srv, &(data[i]) );
        /*lint -restore */
      }
    }
  }

  /* XDR OP NUMBER = 4 */
  if ( xdr_status )
  {
    xdr_op_number = 4;
    xdr_status = XDR_RECV_UINT32( srv, &size_words );
  }

  /* XDR OP NUMBER = 5 */
  if ( xdr_status )
  {
    xdr_op_number = 5;
    xdr_status = XDR_RECV_UINT32( srv, &sum );
  }

  if ( xdr_status )
  {
    xdr_op_number++;
  }      

  if ( ! XDR_MSG_DONE( srv ) || ! xdr_status ) {
    XDR_OP_ERR( srv, xdr_op_number );
    // send svcerr_decode reply
    svcerr_decode( srv );
    return;
  }

  /*lint -save -e611*/
  cmd_cb_func = (ping_mdm_data_cb_f_type *) rpc_clnt_callback_lookup( cb_id );
  /*lint -restore */

  if ( cmd_cb_func == NULL ) {
    // send svcerr_systemerr reply
    svcerr_systemerr( srv );
    return;
  }

  cmd_cb_func_result = cmd_cb_func(data, size_words, sum);

  /* XDR OP NUMBER = 1 */
  xdr_op_number = 1;
  xdr_status = xdr_reply_msg_start( srv, &Ping_mdm_rpccbVerf );

  /* XDR OP NUMBER = 2 */
  if ( xdr_status )
  {
    xdr_op_number = 2;
    xdr_status = XDR_SEND_BOOLEAN( srv, &cmd_cb_func_result );
  }

  if ( ! xdr_status )
  {
    XDR_OP_ERR( srv, xdr_op_number );
    // send svcerr_systemerr reply
    svcerr_systemerr( srv );
    return;
  }

  if ( ! XDR_MSG_SEND( srv, NULL ) ) {
    XDR_MSG_SEND_ERR( srv, NULL );
  }
} /* ping_mdm_data_cb_f_type_svc_0x00010002 */

